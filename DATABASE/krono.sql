create database krono;
use krono;	

create table localidade (
cep CHAR(9) NOT NULL PRIMARY KEY,
cidade VARCHAR(100) NOT NULL,
bairro VARCHAR(100) NOT NULL,
logradouro VARCHAR(100) NOT NULL,
municipio VARCHAR(100) NOT NULL,
UF CHAR(2) NOT NULL 
) ENGINE = INNODB;

create table usuario (
id_usuario INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
nome_usuario VARCHAR(100) NOT NULL,
senha_usuario VARCHAR(100) NOT NULL,
dt_cadastro_usuario DATE,
status_usuario CHAR(1),
nivel_usuario CHAR(1),
foto_usuario VARCHAR(100)
) ENGINE = INNODB;

create table escola (
id_escola INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
nome_escola VARCHAR(100) NOT NULL,
nome_responsavel VARCHAR(100) NOT NULL,
cnpj CHAR(14) NOT NULL,
email VARCHAR(100) NOT NULL,
telefone VARCHAR(20) NOT NULL,
cep CHAR(9),
numero VARCHAR(100) NOT NULL,
complemento VARCHAR(100) NOT NULL,			
id_usuario INT,
FOREIGN KEY (cep) REFERENCES localidade(cep),
FOREIGN KEY (id_usuario) REFERENCES usuario(id_usuario)
) ENGINE = INNODB;

create table regra_disp (
id_regra INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
nome_regra VARCHAR(100) NOT NULL,
descricao TEXT NOT NULL,
importante VARCHAR(100),
ativo CHAR(1),
id_escola INT NOT NULL,
FOREIGN KEY (id_escola) REFERENCES escola (id_escola)
) ENGINE = INNODB;

create table plano(
id_plano INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
nome_plano VARCHAR(100) NOT NULL,
dt_inicio DATE NOT NULL,
dt_final DATE NOT NULL
) ENGINE = INNODB;

create table forma_pagamento(
id_fpgto INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
nome_fpgto VARCHAR(100)
) ENGINE = INNODB;

create table pagamento(
num_nota_fiscal INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
dt_pagamento DATE,
valor_total DECIMAL NOT NULL,
id_plano INT,
id_fpgto INT,
id_escola INT,
FOREIGN KEY (id_plano) REFERENCES plano(id_plano),
FOREIGN KEY (id_fpgto) REFERENCES forma_pagamento(id_fpgto),
FOREIGN KEY (id_escola) REFERENCES escola(id_escola)
) ENGINE = INNODB;

create table grade_horaria_escolar (
id_grade_horaria_escolar INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
turno CHAR(1),
tempo INT,
duracao INT,
horario_inicio TIME,
horario_final TIME,
dia_semana VARCHAR(20) NOT NULL,
dt_cadastro DATETIME NOT NULL,
id_escola INT NOT NULL,
FOREIGN KEY (id_escola) REFERENCES escola(id_escola)
) ENGINE = INNODB;

create table curso (
id_curso INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
nome_curso VARCHAR(100) NOT NULL,
sigla_curso VARCHAR(5) NOT NULL,
id_escola INT NOT NULL,
FOREIGN KEY (id_escola) REFERENCES escola (id_escola)
) ENGINE = INNODB;

create table disciplina(
id_disciplina INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
nome_disciplina VARCHAR(100) NOT NULL,
sigla_disciplina VARCHAR(5) NOT NULL,
carga_horaria INT NOT NULL,
id_escola INT NOT NULL,
FOREIGN KEY (id_escola) REFERENCES escola (id_escola)
) ENGINE = INNODB;

CREATE TABLE vinculo_curso_disc (
	id_curso INT NOT NULL,
	id_disciplina INT NOT NULL,
	PRIMARY KEY (id_curso, id_disciplina),
	FOREIGN KEY (id_curso) REFERENCES curso (id_curso),
	FOREIGN KEY (id_disciplina) REFERENCES disciplina (id_disciplina)
) ENGINE = INNODB;

create table turma (
id_turma INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
nome_turma VARCHAR(100) NOT NULL,
serie_turma INT NOT NULL,
id_escola INT,
id_curso INT,
FOREIGN KEY (id_escola) REFERENCES escola(id_escola),
FOREIGN KEY (id_curso) REFERENCES curso(id_curso)
) ENGINE = INNODB;


create table professor (
id_professor INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
nome_professor VARCHAR(100) NOT NULL,
email_professor VARCHAR(100) NOT NULL,
telefone_professor VARCHAR(20) NOT NULL,
cpf_professor VARCHAR(20) NOT NULL,
id_usuario INT,
FOREIGN KEY (id_usuario) REFERENCES usuario(id_usuario)
) ENGINE = INNODB;

CREATE TABLE carga_horaria_esc (
   id_carga_horaria_esc INT AUTO_INCREMENT PRIMARY KEY,
   id_escola INT NOT NULL,
   horas_semanais INT,
   FOREIGN KEY (id_escola) REFERENCES escola (id_escola)
);

CREATE TABLE vinculo_prof_esc (
	id_escola INT NOT NULL,
	id_professor INT NOT NULL,
	PRIMARY KEY (id_escola, id_professor),
	FOREIGN KEY (id_escola) REFERENCES escola (id_escola),
	FOREIGN KEY (id_professor) REFERENCES professor (id_professor)
) ENGINE = INNODB;

CREATE TABLE vinculo_prof_disc (
	id_professor INT NOT NULL,
	id_disciplina INT NOT NULL,
	PRIMARY KEY (id_disciplina, id_professor),
	FOREIGN KEY (id_disciplina) REFERENCES disciplina (id_disciplina),
	FOREIGN KEY (id_professor) REFERENCES professor (id_professor)
) ENGINE = INNODB;

CREATE TABLE vinculo_prof_carga (
	id_professor INT NOT NULL,
	id_carga_horaria_esc INT NOT NULL,
	PRIMARY KEY (id_professor, id_carga_horaria_esc),
	FOREIGN KEY (id_professor) REFERENCES professor (id_professor),
	FOREIGN KEY (id_carga_horaria_esc) REFERENCES carga_horaria_esc (id_carga_horaria_esc)
) ENGINE = INNODB;

create table grade_disp_prof(
id_grade_disp_prof INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
id_professor INT,
id_escola INT,
dia_semana VARCHAR(20) NOT NULL,
tempo INT,
turno CHAR(1),
tipo_disp CHAR(1) NOT NULL,
dt_cadastro DATETIME NOT NULL,
ativo CHAR(1),
FOREIGN KEY (id_professor) REFERENCES professor(id_professor),
FOREIGN KEY (id_escola) REFERENCES escola(id_escola)
) ENGINE = INNODB;

create table grade_horaria_professor(
id_grade_horaria_professor INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
turno CHAR(1),
tempo INT,
duracao INT,
horario_inicio TIME,
horario_final TIME,
dt_cadastro DATETIME NOT NULL,
dia_semana VARCHAR(20) NOT NULL,
id_professor INT NOT NULL,
id_disciplina INT,
FOREIGN KEY (id_professor) REFERENCES professor(id_professor),
FOREIGN KEY (id_disciplina) REFERENCES disciplina(id_disciplina)
) ENGINE = INNODB;

CREATE TABLE grade_hora_turma (
	id_grade_hora_turma INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
	dia_semana CHAR(1),
	id_disciplina INT NOT NULL,
	id_turma INT NOT NULL,
	id_curso INT,
	id_professor INT NOT NULL,
	dt_cadastro DATETIME NOT NULL,
	tempo INT,
	turno CHAR(1),
	FOREIGN KEY (id_disciplina) REFERENCES disciplina (id_disciplina),
	FOREIGN KEY (id_turma) REFERENCES turma (id_turma),
	FOREIGN KEY (id_curso) REFERENCES curso (id_curso),
	FOREIGN KEY (id_professor) REFERENCES professor (id_professor)
);

-- Criação da tabela de notificações
CREATE TABLE IF NOT EXISTS notificacao (
    id_notificacao INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    id_remetente INT NOT NULL,
    id_destinatario INT NOT NULL,
    mensagem TEXT NOT NULL,
    data_envio DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (id_remetente) REFERENCES usuario(id_usuario),
    FOREIGN KEY (id_destinatario) REFERENCES usuario(id_usuario)
) ENGINE = INNODB;	

